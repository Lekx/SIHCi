	<p>
	   La generación de conocimiento es un bien en sí	 mismo: las sociedades más desarrolladas del
	   mundo reconocen la importancia del conocimiento científico y dedican recursos humanos y
	   financieros para impulsar esta actividad. Tradicionalmente, los investigadores realizan estudios
	   que generan conocimiento científico nuevo; éste es comunicado a través presentaciones encongresos y reuniones científicas, y es difundido,
	   en una proporción variable, en publicaciones científicas que usualmente “leen” otros investigadores 
	   para realizar más estudios y generar más conocimiento, y así sucesivamente (Figura 1.1).
     </p>
     <label>Img Center delet</label>
	 <img src="Img1.1.jpg">
	 
	 <p>
		Aun cuando el principal vehículo de comunicación en
		las ciencias de la salud es el artículo original,numerosas
		investigaciones científicas no culminan en la publicación; 
		el factor limitante más importante es, precisamente,el acto de 
		escribir el artículo científico. En la actualidad, en México y Latinoamérica, 
		se aprecia una desproporción considerable entre el número de protocolos de
		investigación registrados, trabajos presentados en congresos y reuniones 
		científicas, y el número de artículos científicos publicados.
	</p>		
	
	<p>
		Las habilidades para escribir artículos científicos no son naturales o espontaneas; se requiere el aprendizaje y
		entrenamiento para en la redacción de artículos científicos, así como la adherencia estricta a ciertos principios. 
		Recientemente se publicó la “Guía de Redacción de Artículos Originales en Ciencias de la Salud” que
		ofrece métodos probados y técnicas comprobadas para	aprender a escribir y publicar resultados de investigación
		en todos los niveles, especialmente para aquellos que nunca han publicado un artículo científico
		original o lo han hecho pero aún se consideran inexpertos; además, ofrece información útil para aquellos
		autores que habiendo publicado artículos originales, desean mejorar sus habilidades de redacción científica;
		incluso quienes son expertos en redacción científica encontrarán en la guía conceptos nuevos o ya conocidos
		pero con enfoque diferente, que permiten hacer más	eficiente la escritura científica (Figura 1.2 y Cuadro 1.1). 
	</P>
