	<p>
		La guía es útil para todas las disciplinas en ciencias de la salud: medicina general, especialidades y
		subespecialidades médicas, odontología, psicología,veterinaria, nutrición, trabajo social, enfermería y
		química, entre otras; puede ser aplicada desde el nivel de licenciatura hasta los grados de maestría 
		y doctorado en ciencias. Todos los profesionales en las diferentes disciplinas de las ciencias 
		de la salud que	realizan o participan en proyectos de investigación, realizados con solidez metodológica, tienen la
		responsabilidad de escribir y publicar los resultados de las investigaciones en revistas científicas 
		para lograr	la difusión-aplicación del conocimiento para beneficio de la sociedad y de las instituciones. 
	</p>

	<p>
		Los aspectos novedosos de la Guía de Redacción se relacionan con la importancia de identificar el
		mensaje principal que es el conocimiento nuevo en ofrecen los resultados de la investigación; se presentan
		mnemotecnias para escribir el Título (PRECISO), los Resultados (DECIR) y la Discusión (4C s y KNOWS),así
		como, técnicas de redacción moderna, en español e inglés, considerando las “tres reglas de oro: tener algo que 
		escribir, escribirlo bien y publicarlo”.
		
		La “Guía de Redacción de Artículos Originales en Ciencias de la Salud” incluye un “Guía Práctica de Redacción de
		Artículos Originales en Ciencias de la Salud” en 12 tarjetas plastificadas, de tamaño pequeño y fácil consulta. La
		Guía conduce por “el camino de la redacción científica” a lograr la difusión e impacto del conocimiento para la 
		innovación en medicina (Cuadro 1.2). 

	</p>

