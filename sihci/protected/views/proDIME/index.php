    Nuestros médicos son reconocidos por el número de enfermos que diagnostican y curan; queremos que también lo sean, por la calidad del conocimiento científico que producen para la innova ción en medicina y los beneficios evidentes que ofrecen soluciones para l os pacientes y la comunidad en general.

